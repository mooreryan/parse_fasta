module ParseFasta
  class Record
    attr_accessor :header, :seq

    def initialize header, seq
      @header = header
      @seq = seq.gsub(/ +/, "")
    end

    def == rec
      self.header == rec.header && self.seq == rec.seq
    end
  end
end
